				the exception that the roll for the conflict is wisdom instead of charisma. This means that persuasion can very quickely swing 
				out of hand, so you need to be cautious when persuading someone! The <em class="keyWord">game master</em> sets the initial 
				<em class="keyWord">persuasion points</em> based on how difficult the person should be to persuade.
