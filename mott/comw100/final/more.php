				(once again determined by the <em class="keyWord">game master</em>). This is a fairly standard system when it comes to 
				combat resolution.  
			</p>

			<h3>The Non Standard Goodies</h3>
			<p>
					Sometimes simply wailing on an enemy is not a valid tactic to defeat them, you have to determine exactly where you want
				to hit them. Similarly succesfully hitting an enemy at different locations has the potential to cause different effects on that enemy
				some hits cause paralisiss, others confusion, and still others massive damage. In order to represent this your 
				<em class="keyWord">game master</em> creates a series of <em class="keyWord">resolution points</em>, 
				called <em class="keyWord">weak points</em>, to represent each location to be
				attacked.Eeach <em class="keyWord">weak point</em> spans from zero to an unkown posotive number. 
				When these <em class="keyWord">resolution points</em> hit zero their corisponding effect will play. 
				It is up to the <em class="keyWord">game master</em> to determin what these effects are. Similarly it
				is up to the player to discover additional <em class="keyWord">resolution points</em> that their combatant has, the 
				<em class="keyWord">game master</em> is not expected to tell them. 
			</p>

			<p class="note">
					These additional <em class="keyWord">resolution points</em> do not have to test the same stat as their cousin 
				<em class="keyWord">hit points</em> perhaps a charicter is trying to hack into an injured robot at their terminal, 
				and the corisponding weak point (the terminal) requires inteligents to attack and exploit.
			</p>
			
