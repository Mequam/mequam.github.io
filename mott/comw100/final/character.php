				<h2>Skills</h2>
				<h3>What are they</h3>
				<p>
						In addition to the base stats deterimining how well your character performs certain actions, characters have a set
					of skills that also affect how lickely a character is to succeed at a particular action. For example, the difficulty of a 
					roll to read a forign langauge might be insanely high, but a character with skill in that language can more easily 
					overcome those odds.
				</p>
				<p>
						This is what skills in the game represent, they are specific actions (or groups of actions) within the pre-existing 
					stat catagories that get benifits when your character performs them. When creating your character, 
					you will also create a group of skills. Any skills that you make have to be approved by 
					your <em class="keyWord">game master</em>.
				</p>
				<h3>How to make them</h3>
				<p>
						The first step in creating a skill is deciding what you want it to do. This step is simply a matter of preference, 
					and is the least "gammy" part of the whole proccess. What do you want your character to be good at or be able to do? 
					That will be your new <em class="keyWord"/>skill</em> (as long as your <em class="keyWord">game master</em> approves 
					it of course).
				</p>
				<p>
					Once you know the skill that you want to create you need to determine how to best represent it in the game.
					There are two different ways that skills are catigorised in the game, as broad skills, which contain a large
					subset of other skills, and specific skills, which are mostely contain only themselfs. For example "theivery" is
					a broad skill where as "pickpocketing" is a specific skill. Where your group draws the line with this will mostlickely
					differ slightly from others, as there is a little room for interpretation involved here, as allways talk to your
					<em class="keyWord">game master</em> before hand.
				</p>
				<p>
					There are several different types of skills that you have the choice to create and pick from based on the type of skill
					that you are aiming for. Each one costs a different amount of <em class="keyWord">experience points</em> to purchase and 
					affects the mechanical aspect of the game in different ways. In addition to this most skills can be obtained more then once, 					     staking their effects on top of eachother for each purchase.
				</p>
	
